library(shiny) 

bmi <- function(peso, altura) {
    return(peso / (altura ^ 2))
}

resul <- function(peso, altura){
    v.bmi <- bmi(peso, altura)
    
    if (v.bmi < 18.5) return("Underweight")
    else if (v.bmi < 25) return ("Normal weight")
    else if (v.bmi < 20) return ("Overweight")
    else return ("Obesity")
    
}


shinyServer(
        function(input, output) {
                
                output$inputweightvalue <- renderPrint({input$weight})
                output$inputheightvalue <- renderPrint({input$height})
                output$estimation <- renderPrint({bmi(input$weight,input$height)})
                output$diagnostic <- renderPrint({resul(input$weight,input$height)})
        } 
)



